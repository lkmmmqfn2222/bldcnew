/* USER CODE BEGIN 1 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim == &htim1)
  {
    offsetCurrentRead();
    HALL_ADCSample();
    hallSwitch();
    if(stateContr.controlMode == 1) //open loop
    {
      stateContr.duty = stateContr.aimDuty;
    }
    else if(stateContr.controlMode == 2)
    {
      stateContr.speedCount++;
      if(stateContr.speedCount > 2)
      {
        piSpd.fdb = hallTree.speedRPMF;
        PID_CALC(piSpd);
        FIRSTORDER_LPF_CACL(piSpd.out,piSpd.outF,0.08379f);
        piSpd.outF = piSpd.out;
        stateContr.speedCount = 0;
        stateContr.duty = piSpd.outF;
      }
    }
    else if(stateContr.controlMode == 3)
    {
      stateContr.speedCount++;
      stateContr.currentCount++;
      if(stateContr.speedCount > 2)
      {
        piSpd.fdb = hallTree.speedRPMF;
        PID_CALC(piSpd);
        FIRSTORDER_LPF_CACL(piSpd.out,piSpd.outF,0.08379f);
        stateContr.speedCount = 0;
      }   
      if(stateContr.currentCount > 1)
      {
        piICurr.ref = TORQUE;
        piICurr.fdb = ADCSampPare.busCurr;
        PID_CALC(piICurr);
        FIRSTORDER_LPF_CACL(piICurr.out,piICurr.outF,0.08379f);
        stateContr.currentCount = 0;
      }
      if(piICurr.outF > piSpd.outF)
      {
        stateContr.duty = piSpd.outF;
        piICurr.ui = piSpd.ui;
      }
      else
      {
        stateContr.duty = piICurr.outF;
        piSpd.ui = piICurr.ui;
      }
    }
  }
}
